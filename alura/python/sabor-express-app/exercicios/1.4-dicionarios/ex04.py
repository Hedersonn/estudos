# exercicio 4 - Crie um dicionário e verifique se uma chave específica existe dentro desse dicionário

pessoa = {
    "nome": "Hederson",
    "idade": 16,
    "cidade": "Londrina"
}

if "nome" in pessoa.keys():
    print("A chave 'nome' esta no dicionario.")
else:
    print("A chave 'nome' nao esta no dicionario.")

